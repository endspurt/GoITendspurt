#Aufgaben:

#Erstellen Sie ein leeres Wörterbuch data.
#Fügen Sie dem Wörterbuch data folgende Schlüssel-Wert-Paare hinzu:
#Schlüssel year mit dem Wert der ganzen Zahl 2024.
#Schlüssel lang mit dem Wert des Strings 'Python'.
#Schlüssel version mit dem Wert der Fließkommazahl 3.12.
#Erwartetes Ergebnis:

#Nach der Ausführung des Programms sollte das Wörterbuch data die drei oben angegebenen Schlüssel-Wert-Paare enthalten.

# Ініціалізація порожнього словника
data = {}

# Додавання пар ключ-значення до словника
data['year'] = 2024         # Додає ключ 'year' зі значенням 2024
data['lang'] = 'Python'     # Додає ключ 'lang' зі значенням 'Python'
data['version'] = 3.12      # Додає ключ 'version' зі значенням 3.12

# Вивід словника для перевірки змісту
print(data)
